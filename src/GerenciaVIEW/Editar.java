    private String getSelectedPriority() {
        if (btnrBaixa.isSelected()) {
            return "baixa";
        } else if (btnrMedia.isSelected()) {
            return "média";
        } else if (btnrAlta.isSelected()) {
            return "alta";
        } else {
            return null;
        }
    }

