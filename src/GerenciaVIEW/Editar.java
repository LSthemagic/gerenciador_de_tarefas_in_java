    private void btnOK_EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOK_EditarActionPerformed
        tarefasDTO objtarefasDTO = new tarefasDTO();
        Update_tarefasDAO updateTarefasDAO = new Update_tarefasDAO();
        String codigo = txt_codigo.getText();
        String titulo = txtTitle.getText();
        String descri = txtDescricao.getText();
        String prioridade = getSelectedPriority();

        if (codigo.isEmpty()) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "Sem o código da tarefa não haverá mudança");
        } else {
            objtarefasDTO.setCodigo(Integer.parseInt(codigo));
            if (cbTarefaClocluida.isSelected()) {
                objtarefasDTO.setConcluido(true);
            }
            if (!titulo.isEmpty()) {
                objtarefasDTO.setTitle(titulo);
            }
            if (!descri.isEmpty()) {
                objtarefasDTO.setDescri(descri);
            }
            if (prioridade != null) {
                objtarefasDTO.setPrioridade(prioridade);
            }

            updateTarefasDAO.updateTarefa(objtarefasDTO);
            JOptionPane.showMessageDialog(null, "Alteração realizada!");
        }
    }//GEN-LAST:event_btnOK_EditarActionPerformed

